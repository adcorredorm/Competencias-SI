package unalcol.agents.examples.labyrinth.multeseo.eater.isi20181.thunderCats.busqueda;

public class Accion{

    private int code;
    private double cost;

    public Accion(int code, double cost){
        this.code = code;
        this.cost = cost;
    }

    public int getCode() {
        return code;
    }

    public double cost() {
        return cost;
    }
}
